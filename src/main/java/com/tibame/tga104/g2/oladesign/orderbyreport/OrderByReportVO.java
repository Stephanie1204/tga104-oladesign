package com.tibame.tga104.g2.oladesign.orderbyreport;

public class OrderByReportVO {

	private String reportYears;
	private String month;
	private String comTaxId;
	private String dayPrice;
	public String getReportYears() {
		return reportYears;
	}
	public void setReportYears(String reportYears) {
		this.reportYears = reportYears;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getComTaxId() {
		return comTaxId;
	}
	public void setComTaxId(String comTaxId) {
		this.comTaxId = comTaxId;
	}
	public String getDayPrice() {
		return dayPrice;
	}
	public void setDayPrice(String dayPrice) {
		this.dayPrice = dayPrice;
	}

}
